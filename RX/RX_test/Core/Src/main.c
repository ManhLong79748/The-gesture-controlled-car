/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void Start_Task1(void  * Param)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {	
		if (Rx_command ==1)// move forward
		{
			RIGHT_FORWARD(1);
			LEFT_FORWARD(1);	
			RIGHT_BACKWARD(0);
			LEFT_BACKWARD(0);
		}
		else if (Rx_command ==2)// move reverse
		{
			RIGHT_FORWARD(0);
			LEFT_FORWARD(0);
			RIGHT_BACKWARD(1);
			LEFT_BACKWARD (1);
		}
		else if (Rx_command ==3)// left
		{
			RIGHT_FORWARD(1);
			LEFT_BACKWARD(1);
			RIGHT_BACKWARD(0);
			LEFT_FORWARD(0);
		}
		else if (Rx_command ==4) // right
		{
			LEFT_FORWARD(1);
			RIGHT_BACKWARD(1);
			LEFT_BACKWARD(0);
			RIGHT_FORWARD(0);
		}
		else if (Rx_command ==0)
		{
			LEFT_FORWARD(0);
			RIGHT_BACKWARD(0);
			LEFT_BACKWARD(0);
			RIGHT_FORWARD(0);	
		}
		else
		{
			LEFT_FORWARD(0);
			RIGHT_BACKWARD(0);
			LEFT_BACKWARD(0);
			RIGHT_FORWARD(0);	
		}
    osDelay(1);	
  }
  /* USER CODE END 5 */
}

